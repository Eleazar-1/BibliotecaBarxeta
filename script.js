// APLICAR IDIOMA
function applyLanguage(lang) {
    const dict = translations[lang] || translations["ca"];
    document.querySelectorAll("[data-i18n]").forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (dict[key]) {
            el.textContent = dict[key];
        }
    });
    localStorage.setItem("biblioteca_lang", lang);
}

// CANVI D’IDIOMA
const langSelector = document.getElementById("langSelector");
if (langSelector) {
    langSelector.addEventListener("change", (e) => {
        applyLanguage(e.target.value);
    });
}

// TEMA CLAR/FOSC
const themeToggle = document.getElementById("themeToggle");
const themeIcon = document.getElementById("themeIcon");

function setTheme(mode) {
    document.body.classList.remove("light", "dark");
    document.body.classList.add(mode);
    localStorage.setItem("biblioteca_theme", mode);
    themeIcon.textContent = mode === "dark" ? "☀️" : "🌙";
}

if (themeToggle) {
    themeToggle.addEventListener("click", () => {
        const current = document.body.classList.contains("dark") ? "dark" : "light";
        const next = current === "dark" ? "light" : "dark";
        setTheme(next);
    });
}

// INICIALITZACIÓ
window.addEventListener("DOMContentLoaded", () => {
    const savedLang = localStorage.getItem("biblioteca_lang") || "ca";
    const savedTheme = localStorage.getItem("biblioteca_theme") || "light";

    if (langSelector) {
        langSelector.value = savedLang;
    }
    applyLanguage(savedLang);
    setTheme(savedTheme);
});